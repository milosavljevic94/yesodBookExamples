import Database.Persist
import Database.Persist.Sqlite
import Database.Persist.TH

share [mkPersist sqlSettings, mkMigrate "migrateAll"] [persistLowerCase|
Person
    name String
    age Int Maybe
    deriving Show
Car
    color String
    make String
    model String
    deriving Show
|]

main :: IO()
main = runSqlite ":memory:" $ do runMigration migrateAll